export const initCadena = () => {
    if (typeof gsap !== 'undefined') {
        gsap.from("#secondary-hero .text", {
            opacity: 0,
            y: 30,
            duration: 1,
            delay: 0.2
        });

        gsap.utils.toArray('.bloque-valor').forEach(block => {
            gsap.from(block, {
                opacity: 0,
                y: 50,
                duration: 0.8,
                scrollTrigger: {
                    trigger: block,
                    start: "top 85%",
                }
            });
        });

        gsap.from("#jovenes-talento", {
            opacity: 0,
            y: 50,
            duration: 1,
            scrollTrigger: {
                trigger: "#jovenes-talento",
                start: "top 80%"
            }
        });
    }
};
