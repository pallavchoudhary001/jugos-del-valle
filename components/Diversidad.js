export const initDiversidad = () => {
    window.scrollTo(0, 0);

    if (typeof gsap !== 'undefined') {
        const tl = gsap.timeline();

        tl.from("#hero img", {
            duration: 1.5,
            scale: 1.2,
            opacity: 0,
            ease: "power2.out"
        })
            .from("#secondary-hero", {
                duration: 1,
                y: 50,
                opacity: 0,
                ease: "power2.out"
            }, "-=0.5")
            .from("#declaracion-section .subtitle", {
                duration: 0.8,
                opacity: 0,
                y: 30,
                ease: "power2.out"
            }, "-=0.3");

        gsap.from(".diversity-card", {
            duration: 0.8,
            y: 60,
            opacity: 0,
            stagger: 0.2,
            ease: "power3.out",
            scrollTrigger: {
                trigger: "#desarrollo-wrap",
                start: "top 80%"
            }
        });

        gsap.from("#footer-relacionadas .cont-li", {
            duration: 0.8,
            y: 40,
            opacity: 0,
            stagger: 0.2,
            ease: "power2.out",
            scrollTrigger: {
                trigger: "#footer-relacionadas",
                start: "top 90%"
            }
        });
    }
};
