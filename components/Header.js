export const initHeader = () => {
	// Sticky Header Logic
	window.addEventListener('scroll', () => {
		const header = document.getElementById('masthead');
		if (window.scrollY > 0) {
			header.classList.add('fixed');
		} else {
			header.classList.remove('fixed');
		}
	});

	// Mobile Menu Toggle
	const toggler = document.querySelector('.navbar-toggler');
	const collapse = document.querySelector('.navbar-collapse');
	if (toggler && collapse) {
		toggler.addEventListener('click', () => {
			collapse.classList.toggle('show');
		});
	}

	// Search Toggle
	const searchBtn = document.querySelector('.search-btn');
	const searchCont = document.querySelector('.cont-search');
	if (searchBtn && searchCont) {
		searchBtn.addEventListener('click', () => {
			searchCont.classList.toggle('active'); // active class usually handles visibility
			// If the live site uses specific jQuery logic, we simulate it here just by toggling display
			if (searchCont.style.display === 'block') {
				searchCont.style.display = 'none';
			} else {
				searchCont.style.display = 'block';
			}
		});
	}

	// Copy desktop menu to mobile if not already populated correctly (simple hack)
	const mobileMenu = document.querySelector('#primary-menu');
	const desktopMenu = document.querySelector('#menu-main');
	if (mobileMenu && desktopMenu && mobileMenu.children.length === 0) {
		mobileMenu.innerHTML = desktopMenu.innerHTML;
	}
}
